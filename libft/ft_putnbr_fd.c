#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	digits[10];
	int		i;

	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = -n;
	}
	i = 9;
	while (n > 9)
	{
		digits[i] = (n % 10) + '0';
		n /= 10;
		i--;
	}
	digits[i] = n + '0';
	write(fd, &digits[i], (10 - i));
}
